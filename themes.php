<h2>Recurring Themes</h2>

<p>Almost every game in the Final Fantasy series shares these common themes, characters and concepts.</p>

<h3>Airships</h3>
<p>Transportation around the various worlds presented in Final Fantasy would have been a problem, if there were not these fancy airships! They are used to transport you around, they were most notably used in FFIV and FFIX, as part of the story line. In the earlier games they appear to look like sailboats with propellers, but in later games seem to look more technological.</p>

<h3>Job System</h3>
<p>In Final Fantasy there are an assortment of different classes the main character or his allies can be. These classes include Warrior, Mage, Monk, Thief and Mime. These can be used in both battles or be used to play a part in the major plot of the game. In later games, characters were able to switch classes, even in the middle of a battle. Legendary Weapons were also seen multiple times, given to each specific class, for example the sword Excalibur for the warrior.</p>

<h3>Status Ailments</h3>
<p>In Final Fantasy each character can be effected by certain ailments that weaken the player. These include silence, poison, pertification and confuse. This is a common theme in most RPGs. With these ailments also come cures, to remove each specific ailment, such as echo herb to cure silence.</p>

<h3>Creatures/Monsters</h3>
<p>Certain creatures appear in most games, two of the most notable are Chocobos (large yellow chickens) and Moogles (Small Mammals). Moogles are known for saying "kupo!" at the end of every sentence, kupo! Also certain monsters reappear in many of the games, such as Goblins and Cactuars. Summoned monsters are also seen time and time again. Some include Leviathan and Shiva. These summoned monsters help you in battle.</p>

<h3>Character Names</h3>
<p>One interesting fact in the Final Fantasy Franchise is the significance of characters names. In most Final Fantasy games, the characters each have a name associated with an element. These names include, Terra (Earth), Cloud (Air), Tidus (Water) and some that are a little more vaguely related, such as Lightning and Squall (small storm).</p>
