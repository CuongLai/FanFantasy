<h2>What is Final Fantasy?</h2>

<p class ='indent'>The Final Fantasy series is one of the most long running series
    of games out there, and as the title implies, it's all about fantasy. The games were 
    first created by a company named Square Soft, and they made the first nine games 
    in the series. They then merged with a separate company named Enix, and the games are now under 
    the control of the aptly named Square Enix. The games fall under the category of RPG, which stands for "role-playing game." In an RPG, 
     you typically take the control of one or more character(s) and go through an epic journey,
      usually driven by some great purpose or goal. Throughout the journey, you'll
      come across many obstacles that you have to fight your way over. These obstacles
      take the form of villains and monsters, which are usually driven by some greater
      force, the main antagonist. The hero's main goal is to rid of the main antagonist
      by fighting and set the land free of the villain's evil hands. The story lines of all the 
      Final Fantasy games follow this path. They each have a main character that will
      go on an epic journey, find partners that will help them along, and defeat an evil
      enemy at the end to save the world.</p>

<p class="indent">The games always take place in some fantasy world, with some being 
    a bit more realistic than others. You take control of a character and travel the world 
    to fight new monsters, level up, become stronger, obtain new equipment, and many other 
    cool things. Each game features a battle system, which is the way you fight the various 
    monsters. Every game has slightly different battle systems, and with the series have more than 
    13 games in the series, the system has evolved so much. Most RPGs have a "standard" battle 
    system, where your characters and the enemy take turns making moves. This is called 
    a "turn-based system," because each character requires a turn to perform an action. 
    The newer, more recent Final Fantasy games have strayed away from this tradition, and 
    are now leaning towards a more action-based, real time battle system. This creates an 
    interest dynamic in the way battles work, and brings a fresh take on the traditional 
    battle system. We have a page detailing the various battle systems.</p>

<p class ='indent'>Being an RPG series, the Final Fantasy games have various aspects
     that make them so interesting and popular. As you navigate through the web site,
      you'll find short descriptions of these various aspects. The "Final Fantasy
       Titles" page has synopses of the various titles for the series, as well as 
       our ratings of them. The "Main Characters" page has descriptions of each main 
       character. The "Battle Systems" page explains the different battle systems 
       in detail. Finally, the "Recurring Themes" page contains facts about the 
       many themes and characters that you will find in a Final Fantasy game.</p>


